// fundamental building blocks in js
// a set of statement that performs a task

function greet() {
  console.log("Hello World");
}

greet();

//arrow function
const greetArrowFunction = () => {
  console.log("Hello World");
};

greetArrowFunction();

function greetWithName(name) {
  console.log("Hello " + name);
}

greetWithName("John");

//arrow function
const greetWithNameArrowFunction = (name) => {
  console.log("Hello " + name);
};

greetWithNameArrowFunction();

